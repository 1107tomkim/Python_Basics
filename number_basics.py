# Changing a number to a string
my_num = 10
print(my_num) # In this print statement my_num is a integer
print(str(my_num)) # In this print statement, my_num is now a string
print(type(my_num)) # Using the type function to check the variable
print(type(str(my_num))) # As you can see the variable changes to a string when using the str() function
print("____________________________________________________") 

