#Arithmetic Operators
# +   -> Addition
def addition(num1, num2):
    return num1 + num2
print(addition(1, 5))

# -   -> Subtraction
def subtraction(num1, num2):
    return num1 - num2
print(subtraction(10, 5))

# *   -> Multiplication
def multiplication(num1, num2):
    return num1 * num2
print(multiplication(2, 2))

# /   -> Division
def division(num1, num2):
    return num1 / num2
print(division(10, 2))

# %   -> Modulus
def modulus(num1, num2):
    return num1 % num2
print(modulus(10, 3))

# **  -> Exponentiation
def exponentiation(num1, num2):
    return num1 ** num2
print(exponentiation(5, 2))

# //  -> Floor Division
def floor_division(num1, num2):
    return num1 // num2
print(floor_division(5, 2))


